// 获取所有故事
function getAllStories() {
    const allStories = [];

    // 合并所有类别的故事
    for (const category in storiesDatabase) {
        allStories.push(...storiesDatabase[category]);
    }

    return allStories;
}

// 根据ID获取故事
function getStoryById(id) {
    for (const category in storiesDatabase) {
        const story = storiesDatabase[category].find(s => s.id === id);
        if (story) return story;
    }
    return null;
}

// 根据类别获取故事
function getStoriesByCategory(category) {
    return storiesDatabase[category] || [];
}

// 根据标签获取故事
function getStoriesByTag(tag) {
    const stories = [];

    for (const category in storiesDatabase) {
        const categoryStories = storiesDatabase[category].filter(s => s.tags.includes(tag));
        stories.push(...categoryStories);
    }

    return stories;
}

// 搜索故事
function searchStories(keyword) {
    const stories = [];

    for (const category in storiesDatabase) {
        const categoryStories = storiesDatabase[category].filter(s =>
            s.title.includes(keyword) ||
            s.content.includes(keyword) ||
            s.tags.some(tag => tag.includes(keyword))
        );
        stories.push(...categoryStories);
    }

    return stories;
}

